<?php
function f1($c)
{
    $c[0] = 5;
}

function f2(& $c)
{
    $c[0] = 5;
}

$a1 = [1,2,3];
$b1 = $a1;
$b1[0] = 4;
print_r($a1);

$a2 = [1,2,3];
$b2 = & $a2;
$b2[0] = 4;
print_r($a2);

$a3 = [1,2,3];
f1($a3);
print_r($a3);

$a4 = [1,2,3];
f2($a4);
print_r($a4);

$d1 = [1,2];
$e1 = [$d1,$d1];
$e1[0][0] = 3;
print_r($e1);

$d2 = [1,2];
$e2 = [& $d2,& $d2];
$e2[0][0] = 3;
print_r($e2);
?>
